/******************************************************************************
* Name: GPIOConfig (pin, mode)
* Description:  Configures the mode of the pin as INPUT and output
* Arguments: pin and mode
* Returns: None
******************************************************************************/
void GPIOConfig(uint8_t Pin,uint8_t Mode)
{
  if(Mode)
    {
        DDRD|=(1<<Pin);
        PORTD&=~(1<<Pin);
    }
  else
    {
        DDRD&=~(1<<Pin);
        PORTD|=(1<<Pin);
    }
}
